#!/usr/bin/env node
/* eslint-disable no-console */
/**
 * `any` audit (read-only).
 *
 * Counts explicit `any` occurrences per top-level directory under `src/`
 * to track progress on IMPROVEMENTS_CHECKLIST.md §9.1. This does NOT
 * fail the build — turning `@typescript-eslint/no-explicit-any` into an
 * error rule across the whole repo would surface hundreds of warnings
 * at once. Instead, this script writes a snapshot to
 * `docs/reports/ANY_USAGE_SNAPSHOT.md` so the trend is reviewable.
 *
 * Usage:
 *   node scripts/audit-any-usage.js          # write report
 *   node scripts/audit-any-usage.js --print  # also print to stdout
 */

const fs = require('node:fs');
const path = require('node:path');

const REPO_ROOT = path.resolve(__dirname, '..');
const SRC_DIR = path.join(REPO_ROOT, 'src');
const OUT_PATH = path.join(
  REPO_ROOT,
  'docs',
  'reports',
  'ANY_USAGE_SNAPSHOT.md'
);

const ANY_RE = /(?<![A-Za-z0-9_$])any(?=[\s,;)\]}>|&]|$)/g;

function* walk(dir) {
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    if (entry.name.startsWith('.')) {
      continue;
    }
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      yield* walk(full);
    } else if (/\.(ts|tsx)$/.test(entry.name) && !/\.d\.ts$/.test(entry.name)) {
      yield full;
    }
  }
}

function countInLine(line) {
  // Strip simple `// ...` comments to reduce false positives.
  const codeOnly = line.replace(/\/\/.*$/, '');
  // Skip JSDoc-only lines.
  if (/^\s*\*/.test(line)) {
    return 0;
  }
  // Don't count `any[]` spelled inside string literals.
  const stripped = codeOnly.replace(/'[^']*'|"[^"]*"|`[^`]*`/g, '');
  const matches = stripped.match(ANY_RE);
  return matches ? matches.length : 0;
}

function main() {
  const buckets = new Map();
  let total = 0;
  let totalFiles = 0;
  for (const file of walk(SRC_DIR)) {
    totalFiles++;
    const rel = path.relative(SRC_DIR, file);
    const top = rel.split(path.sep)[0] || '<root>';
    let count = 0;
    const text = fs.readFileSync(file, 'utf8');
    for (const line of text.split('\n')) {
      count += countInLine(line);
    }
    if (count > 0) {
      buckets.set(top, (buckets.get(top) || 0) + count);
      total += count;
    }
  }

  const sorted = [...buckets.entries()].sort((a, b) => b[1] - a[1]);

  const lines = [];
  lines.push('# `any` Usage Snapshot');
  lines.push('');
  lines.push(
    `Generated by \`scripts/audit-any-usage.js\` on ${new Date().toISOString()}.`
  );
  lines.push('');
  lines.push(`- TS/TSX files scanned: **${totalFiles}**`);
  lines.push(`- Total \`any\` occurrences (heuristic): **${total}**`);
  lines.push('');
  lines.push('## Top-level breakdown');
  lines.push('');
  lines.push('| Top-level dir under `src/` | `any` count |');
  lines.push('|----------------------------|-------------|');
  for (const [dir, count] of sorted) {
    lines.push(`| \`${dir}\` | ${count} |`);
  }
  lines.push('');
  lines.push('## Notes');
  lines.push('');
  lines.push(
    '- This is a **heuristic** (regex over source text); expect ~5% noise from comments and template strings.'
  );
  lines.push(
    '- The lint rule `@typescript-eslint/no-explicit-any` is currently **warning-only** repo-wide. Promoting it to error is tracked in IMPROVEMENTS_CHECKLIST.md §9.1 and should be done after the count drops materially in shared infra (`src/lib`, `src/core`, `src/modules/shared`).'
  );
  lines.push(
    "- Route-factory delegates already use the `(...args: any[])` rest-param pattern intentionally because Prisma's `SelectSubset` generics are not structurally expressible — those occurrences are sound and can be filtered out with a per-file `eslint-disable` line if needed."
  );

  fs.mkdirSync(path.dirname(OUT_PATH), { recursive: true });
  fs.writeFileSync(OUT_PATH, lines.join('\n') + '\n', 'utf8');
  console.log(
    `[audit-any-usage] Total: ${total} across ${totalFiles} files. Wrote ${path.relative(REPO_ROOT, OUT_PATH)}`
  );
  if (process.argv.includes('--print')) {
    console.log('');
    for (const [dir, count] of sorted) {
      console.log(`  ${dir.padEnd(20)} ${count}`);
    }
  }
}

main();
